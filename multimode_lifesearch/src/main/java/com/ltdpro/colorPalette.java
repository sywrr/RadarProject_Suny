	public int getColorNumber()
	{
		if( mCurrentColPalIndex < mHighColpalNumber )
			return mColNumber;
		else if( mCurrentColPalIndex < mMiddleColpalNumber + mHighColpalNumber 
				&& mCurrentColPalIndex >= mHighColpalNumber )		
			return mMiddleColNumber;
		else return g_lowPalettePrecision;
	}

